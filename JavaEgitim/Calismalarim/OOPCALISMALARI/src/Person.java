public class Person {

    private String name;
    private int age;
    private String surname;

    //getter metodlari
    public String getName() {
        return name;

    }
public String getSurname(){
        return surname;

}

public void setSurname(String surname){
        this.surname = surname;
}
    public int getAge(){
        return age;

    }

    //setter metotlari
    public void setName(String name){
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
